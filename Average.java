public class Average {
    public static void main(String[]args) {
        System.out.println("Compute using same Data Type"); 
        double average=0;
        double quiz_1=40, quiz_2=3;
        average = (quiz_1+quiz_2)/2;
        System.out.println("The average of " + quiz_1 + " and "+ quiz_2 + " is " + average);
    }
}


